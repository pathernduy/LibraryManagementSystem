    public void showTableWithIssuedBooks(String status){
        ArrayList<Issue_Book> isList  = issue.issuebookList(status);
        
        //column names
        String[] colNames = {"Book Id","Member Id","Status","Issued Date","Return Date","Note"};
        
        //row name
        Object[][] rows = new Object[isList .size()][colNames.length]; 
        
        
        for(int i=0; i < isList.size();i++){
            rows[i][0]=isList .get(i).getBook_id();
            rows[i][1]=isList .get(i).getMember_id();
            rows[i][2]=isList .get(i).getStatus();
            rows[i][3]=isList .get(i).getIssue_date();
            rows[i][4]=isList .get(i).getReturn_date();
            rows[i][5]=isList .get(i).getNote();
        }
        
        DefaultTableModel model = new DefaultTableModel(rows,colNames);
        jTable_ReturnBook.setModel(model);
                
    }
    
