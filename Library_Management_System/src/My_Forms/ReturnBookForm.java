    private void jButton_ReturnBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ReturnBookActionPerformed
        try {
            int bookId = (int)jSpinner_BookId.getValue();
            int memberId = (int)jSpinner_MemberId.getValue();

            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            String issueDate = dateFormat.format(jDateChooser_IssueDate.getDate()); 
            String returnDate = dateFormat.format(jDateChooser_ReturnDate.getDate()); 
            String note = jTextArea_Note.getText();

            Date issDate = dateFormat.parse(issueDate);
            Date retDate = dateFormat.parse(returnDate);
            
            if(retDate.before(issDate)){
                JOptionPane.showMessageDialog(null, "The return date must be after issue date", "Wrong Return Date",2);
            }
            else{
                issue.updateIssue(bookId, memberId, "returned", issueDate, returnDate, note);
                jSpinner_BookId.setValue(0);
                jSpinner_MemberId.setValue(0);
                jLabel_BookName.setText("Book name");
                jLabel_MemberName.setText("Member Full-name");
                jLabel_CheckAvailabilityBook.setText("Yes - No");
                jLabel_CheckAvailabilityBook.setForeground(new Color(0,255,255));
                jDateChooser_IssueDate.setDate(new Date());
                jDateChooser_ReturnDate.setDate(new Date());
                jTextArea_Note.setText("");
            }
        
        } catch (ParseException ex) {
            Logger.getLogger(ReturnBookForm.class.getName()).log(Level.SEVERE, null, ex);
        }catch (NullPointerException ex) {
            JOptionPane.showMessageDialog(null, "Select issued book to update", "Update status error",2);
        }
    }//GEN-LAST:event_jButton_ReturnBookActionPerformed

